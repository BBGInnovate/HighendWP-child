				<h2>Election Day</h2>
<div id="images">
    <a href="https://www.bbg.gov/wp-content/media/2016/11/Alhurra_election-coverage_Dearborn-Michigan-1040x624.jpg">
        <img src="https://www.bbg.gov/wp-content/media/2016/11/Alhurra_election-coverage_Dearborn-Michigan-1040x624.jpg" width="600px" height="120px">
        <div class="caption">Alhurra correspondent in Dearborn, Michigan.
</div>
    </a>				
<p>Audiences around the world, including in some of the most press-restrictive countries, had access to full, comprehensive coverage of the historic 58th U.S. presidential election, thanks to the networks of the BBG.</p>
<p>The VOA — RFE/RL Current Time digital network offered its audience <a href="https://www.youtube.com/watch?v=GSpC0TZV13c" style="color:#0645AD;">eight hours of live Russian-language coverage</a> on election night.</p>
<p>Several of VOA’s African services connected diaspora communities online during for election night parties. VOA Swahili, for example, went live on Facebook as members of the diaspora from East Africa watched the results come in from a Kenyan restaurant in Beltsville, Maryland.</p>
<p>Middle East Broadcasting Network's Alhurra Television stationed <a href="https://www.facebook.com/alhurra/videos/10154534735971136/" style="color:#0645AD;">its correspondents throughout the Middle East and North Africa</a>, featuring reactions from Afghanistan, Iraq, Lebanon, Turkey, Iran and elsewhere. The network’s flagship discussion program <a href="https://www.youtube.com/watch?v=pqRCo9p-Eks&feature=youtu.be" style="color:#0645AD;">Free Hour</a> was expanded an additional hour to feature discussion from Republican and Democratic analysts and foreign policy experts from Iraq, Saudi Arabia, Egypt and Iran.</p>
<p>RFE/RL's Radio Farda provided its radio and Internet audiences with <a href="https://www.radiofarda.com/a/f4_us_election_start_vote_new_president_clinton_trump/28103858.html" style="color:#0645AD;">live updates</a> of the results, as well as a series of features, such as the history of Iranian-American relations with Democratic and Republican administrations in the U.S.</p>
<p>Radio Azadi, broadcasting in both Dari and Pashto to Afghanistan, liveblogged on election night and conducted interviews with numerous prominent Afghan politicians and residents about expectations for the vote. Azadi also aired a discussion that included <a href="https://www.facebook.com/AzadiR/videos/1185185491574776/" style="color:#0645AD;">Afghanistan President Ashraf Ghani’s press advisor</a>, Nabi Misdaq.</p>
<p>The RFE/RL newsroom also produced <a href="https://www.rferl.org/a/how-does-the-us-electoral-college-work/28090772.html" style="color:#0645AD;">a video explainer</a>, both in English and Russian, on the U.S. Electoral College, as well as a glossary of key terms and phrases.</p>
<p>TV and Radio Martí aired more than 20 hours of live continuous coverage and 24-hour live blogging through social media. As part of their coverage, Radio and TV Martí interviewed Cuban immigrants who were voting for the first time, as well as, <a href="https://www.martinoticias.com/a/apoyo-a-trump-divide-voto-cubanoamericano/132630.html" style="color:#0645AD;">other Cuban voters across the country.</a> The Martís also interviewed <a href="https://www.martinoticias.com/a/por-quien-se-inclinan-cubanos-isla-hillary-trump/133208.html" style="color:#0645AD;">Cubans in the island </a>for their thoughts on the election.</p>
<p>RFA’s Mandarin service covered the election with dispatches from Hong Kong. Even though the Chinese authorities barred live coverage of the vote-counting process in the U.S., the election attracted widespread attention in China.</p>
<p>VOA Korean <a href="http://www.voanews.com/a/heated-us-election-north-korean-refugees-taste-democracy/3569011.html" style="color:#0645AD;">shared a story </a>with RFAs’ Korean service on a North Korean refugee, resettled in Virginia, as he voted for the first time.</p>
